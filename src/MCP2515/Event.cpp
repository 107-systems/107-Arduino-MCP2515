/**************************************************************************************
 * PUBLIC MEMBER FUNCTIONS
 **************************************************************************************/

void Event::begin()
{
  pinMode(_int_pin, INPUT_PULLUP);
  attachInterrupt(digitalPinToInterrupt(_int_pin), Event::onExternalEvent, FALLING);
}

